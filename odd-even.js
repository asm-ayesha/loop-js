/***

Subtask-1:

Find all the odd numbers from 61 to 100.

 */

let oddNum = 61;
while(oddNum <= 100){
    console.log(oddNum)
    if(oddNum % 2 == 1){
        console.log('Odd number is: ',oddNum)
    }
    oddNum ++;
}



